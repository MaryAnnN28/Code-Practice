// The .unshift() method

// - This method adds one or more elements to the beginning of an array and returns the new length of the array. 


const shoppingList = ["Spinach", "Eggs", "Bread"];

console.log(shoppingList.unshift("Bananas")); //  returns 4 because our shopping list now has 4 items 
console.log(shoppingList); // returns ["Bananas", "Spinach", "Eggs", "Bread"]