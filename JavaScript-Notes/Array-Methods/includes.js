// Includes Method    .includes()

// - Directly tells us whether an array includes that element directly. 
// - Checks for the elements existence, and only returns a boolean, true or false 
//    + Only works with primitive values like strings and booleans, not objects 

const temperatures = [69, 82, 73, 64]; // 0, 1, 2, 3; -1

console.log(temperatures.includes(64)); // returns true 